# 법제처 해석례 번호: 26-0202, 22-0379, 09-0041 등 (NN-NNNN)
# \b 대신 lookaround 사용 — 한글이 인접해도 매칭되도록 (예: "26-0202와")
_LAWBUREAU_PATTERN = re.compile(r'(?<!\d)(\d{2}-\d{4})(?!\d)')

# 대법원 판례: 2011다83431, 2001두274, 2017두73693, 99두592 등 (YY+한글1자+숫자)
_COURT_CASE_PATTERN = re.compile(r'(?<![가-힣\d])(\d{2,4}[가-힣]\d{3,5})(?!\d)')


def extract_test_exclusions(query: str) -> list[str]:
    """
    질문에 인용된 판례·해석례 번호를 추출 (테스트 모드 제외 목록).

    트리거: 정답을 알고 있는 사용자가 동일 질문과 출처를 함께 붙여넣어
            AI 답변이 그 결론에 독립적으로 도달할 수 있는지 검증할 때 사용.
    """
    excl: set[str] = set()
    excl.update(_LAWBUREAU_PATTERN.findall(query))
    excl.update(_COURT_CASE_PATTERN.findall(query))
    return sorted(excl)


def _memo_linked_str(m: dict) -> str:
    """memo의 linked_to를 문자열로 통일 (list 또는 string)."""
    lt = m.get("linked_to", "")
    if isinstance(lt, list):
        return ",".join(str(x) for x in lt)
    return str(lt)


# ── 답변 후처리: 검색으로 확인되지 않은 인용번호 차단 ──────────────
# 모델(gemini)이 파라미터에 각인한 판례·해석례를 환각 인용하는 것을 출력 단에서
# 결정론적으로 막는다. 프롬프트·파라미터·질문 어느 소스의 환각이든 일괄 차단.
# 날짜·"법제처/대법원"·"회신/선고/판결" 수식어까지 묶어 자연 표현으로 치환.
_QA_CITE_FULL = re.compile(
    r'(?:법제처\s*)?(?:\d{4}\.\s*\d{1,2}\.\s*\d{1,2}\.\s*회신\s*)?'
    r'(?<!\d)(\d{2}-\d{4})(?!\d)(?:\s*(?:해석례|회신))?'
)
_COURT_CITE_FULL = re.compile(
    r'(?:대법원\s*)?(?:\d{4}\.\s*\d{1,2}\.\s*\d{1,2}\.\s*선고\s*)?'
    r'(?<![가-힣\d])(\d{2,4}[가-힣]\d{3,5})(?!\d)(?:\s*(?:전원합의체\s*)?판결(?:례)?)?'
)
# 번호 없는 날짜형 인용(기관 + 날짜 + 회신/판결류). 식별 번호가 없어 검증 불가
# → 사실상 환각이므로 일반 표현으로 치환. 뒤에 번호가 따라오면(검증 가능) 제외.
_DATED_NONUM_CITE = re.compile(
    r'(법제처|국토교통부|국토부|행정안전부|행정자치부|대법원|헌법재판소)\s*'
    r'\d{4}\.\s*\d{1,2}\.\s*\d{1,2}\.?\s*'
    r'(?:선고\s*)?(질의회신|질의|회신|판결|결정|유권해석)'
    r'(?!\s*[\d가-힣]*\s*\d)'
)
# 부처 부서 민원회신·유권해석: "건축기획팀-2471", "도시정책과-1234"(부서명-번호[, 날짜]).
# 우리가 원문을 보유하지 않는 형식 → allowed_codes에 없으면 환각으로 보고 일반 표현 치환.
_AGENCY_DOC_CITE = re.compile(
    r'(?<![가-힣])([가-힣]{2,12}(?:팀|과|국|실|담당관))-(\d{2,6})'
    r'(?:\s*,?\s*\d{4}\.\s*\d{1,2}\.\s*\d{1,2}\.?)?(?:\s*참조)?'
)


def strip_unverified_citations(answer: str, allowed_codes: set) -> tuple[str, list[str]]:
    """답변에서 검색 컨텍스트(allowed_codes)에 없는 인용번호를 일반 표현으로 치환.

    반환: (정제된 답변, 제거된 번호 목록).
    """
    removed: list[str] = []

    def _qa(m):
        code = m.group(1)
        if code in allowed_codes:
            return m.group(0)
        removed.append(code)
        return "관련 법제처 해석례"

    def _court(m):
        code = m.group(1)
        if code in allowed_codes:
            return m.group(0)
        removed.append(code)
        return "관련 대법원 판례"

    def _dated(m):
        org, kind = m.group(1), m.group(2)
        removed.append(m.group(0).strip()[:30])
        is_court = ("판결" in kind or "결정" in kind or org in ("대법원", "헌법재판소"))
        return f"관련 {org} 판례" if is_court else f"관련 {org} 회신"

    def _agency(m):
        key = f"{m.group(1)}-{m.group(2)}"   # "건축기획팀-2471"
        if key in allowed_codes:
            return m.group(0)
        removed.append(key)
        return "관련 행정해석"

    out = _COURT_CITE_FULL.sub(_court, answer)   # 판례(YY+한글+숫자) 먼저
    out = _QA_CITE_FULL.sub(_qa, out)            # 해석례(NN-NNNN) 다음
    out = _DATED_NONUM_CITE.sub(_dated, out)     # 번호 없는 날짜형 인용
    out = _AGENCY_DOC_CITE.sub(_agency, out)     # 부처 부서 민원회신(부서명-번호)
    return out, removed


