def _gemma_options(temperature: float = 1.0) -> dict:
    return {
        "temperature": temperature,
        "num_ctx":     GEMMA_NUM_CTX,
    }


def call_gemma(client, system: str, user_msg: str, temperature: float = 1.0) -> str:
    """Ollama 비스트리밍 호출. client는 미사용(API 일관성 위해)."""
    try:
        import requests
        r = requests.post(
            f"{GEMMA_URL.rstrip('/')}/api/chat",
            json={
                "model": GEMMA_MODEL_NAME,
                "messages": [
                    {"role": "system", "content": system},
                    {"role": "user",   "content": user_msg},
                ],
                "stream":  False,
                "options": _gemma_options(temperature),
            },
            timeout=600,
        )
        r.raise_for_status()
        data = r.json()
        return data.get("message", {}).get("content", "") or ""
    except Exception as e:
        return f"[Gemma 로컬 오류] {e}"


def call_gemma_stream(client, system: str, user_msg: str, stream_callback, temperature: float = 0.2) -> str:
    """Ollama 스트리밍 호출. 응답은 newline-delimited JSON."""
    try:
        import requests
        full_text = ""
        with requests.post(
            f"{GEMMA_URL.rstrip('/')}/api/chat",
            json={
                "model": GEMMA_MODEL_NAME,
                "messages": [
                    {"role": "system", "content": system},
                    {"role": "user",   "content": user_msg},
                ],
                "stream":  True,
                "options": _gemma_options(temperature),
            },
            stream=True,
            timeout=600,
        ) as r:
            r.raise_for_status()
            for line in r.iter_lines(decode_unicode=True):
                if not line:
                    continue
                try:
                    chunk = json.loads(line)
                except Exception:
                    continue
                token = chunk.get("message", {}).get("content", "")
                if token:
                    full_text += token
                    if stream_callback:
                        stream_callback(token)
                if chunk.get("done"):
                    break
        return full_text
    except Exception as e:
        return f"[Gemma 로컬 오류] {e}"


