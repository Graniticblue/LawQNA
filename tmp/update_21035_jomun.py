import sys, json
sys.stdout.reconfigure(encoding='utf-8')
from pathlib import Path

path = Path('data/law_amendments/amendments.jsonl')
records = [json.loads(l) for l in path.read_text(encoding='utf-8').splitlines() if l.strip()]

for r in records:
    if r['amendment_id'] != '건축법_20260227_21035호':
        continue

    # 1. 개정전 기준 법령 추가
    r['개정전_기준법령'] = "건축법 [시행 2024. 6. 27] [법률 제20424호, 2024. 3. 26, 일부개정]"

    # 2. 제22조②단서 — 단어만 있던 것을 전문으로 교체
    for item in r['조문_변경']:
        if item['조문'] == '제22조제2항 단서':
            item['개정전'] = (
                "다만, 해당 지방자치단체의 조례로 정하는 건축물은 "
                "사용승인을 위한 검사를 실시하지 아니하고 사용승인서를 내줄 수 있다."
            )
            item['개정후'] = (
                "다만, 해당 지방자치단체의 조례로 정하는 건축물"
                "(국토교통부령으로 정하는 숙박시설은 제외한다)은 "
                "사용승인을 위한 검사를 실시하지 아니하고 사용승인서를 내줄 수 있다."
            )
        if item['조문'] == '제22조제1항':
            item['개정전'] = (
                "건축주가 제11조·제14조 또는 제20조제1항에 따라 허가를 받았거나 신고를 한 건축물의 건축공사를 완료"
                "[하나의 대지에 둘 이상의 건축물을 건축하는 경우 동(棟)별 공사를 완료한 경우를 포함한다]한 후 "
                "그 건축물을 사용하려면 제25조제6항에 따라 공사감리자가 작성한 감리완료보고서"
                "(같은 조 제1항에 따른 공사감리자를 지정한 경우만 해당된다)와 "
                "국토교통부령으로 정하는 공사완료도서를 첨부하여 허가권자에게 사용승인을 신청하여야 한다."
            )
            item['개정후'] = (
                "건축주가 제11조·제14조 또는 제20조제1항에 따라 허가를 받았거나 신고를 한 건축물의 건축공사를 완료"
                "[하나의 대지에 둘 이상의 건축물을 건축하는 경우 동(棟)별 공사를 완료한 경우를 포함한다]한 후 "
                "그 건축물을 사용하려면 제25조제6항에 따라 공사감리자가 작성한 감리완료보고서"
                "(같은 조 제1항에 따른 공사감리자를 지정한 경우만 해당된다)와 "
                "공사완료도서 등 국토교통부령으로 정하는 서류를 첨부하여 허가권자에게 사용승인을 신청하여야 한다."
            )
    break

with open(path, 'w', encoding='utf-8') as f:
    for r in records:
        f.write(json.dumps(r, ensure_ascii=False) + '\n')

